package Day012;

public class Day012_3 {

	public static void main(String[] args) {

		for(int i=1;i<=3;i++) {
			System.out.print("JAVA"+i+"\t");
		}
		System.out.println();
		int i =1;
		while(i<=3) {
			System.out.print("JAVA"+i+"\t");
			i++;
		}
		System.out.println();
		i =1;
		do {
			System.out.print("JAVA"+i+"\t");
			i++;
		}while(i<=3);
	}

}
